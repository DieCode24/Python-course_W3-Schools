# Asignación inicial de variables
x = 5        # x es de tipo int (entero) y tiene el valor 5
y = "John"   # y es de tipo str (cadena) y tiene el valor "John"

# Imprimir los valores de las variables
print(x)     # Esto imprimirá: 5
print(y)     # Esto imprimirá: John

# Reasignación de la variable x
x = 4        # x es de tipo int (entero) y tiene el valor 4
x = "Sally"  # x ahora es de tipo str (cadena) y tiene el valor "Sally"
print(x)     # Esto imprimirá: Sally

# Conversión de tipos de datos
x = str(3)    # x será '3' (cadena)
y = int(3)    # y será 3 (entero)
z = float(3)  # z será 3.0 (número de punto flotante)

# Uso de la función type() para verificar los tipos de datos
x = 5         # x es de tipo int (entero) y tiene el valor 5
y = "John"    # y es de tipo str (cadena) y tiene el valor "John"
print(type(x)) # Esto imprimirá: <class 'int'>
print(type(y)) # Esto imprimirá: <class 'str'>

# Asignación de cadenas usando comillas simples y dobles
x = "John"    # Asignación de cadena usando comillas dobles
# es lo mismo que
x = 'John'    # Asignación de cadena usando comillas simples

# Sensibilidad a mayúsculas en nombres de variables
